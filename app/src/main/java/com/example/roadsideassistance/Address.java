package com.example.roadsideassistance;

public class Address {
    public int streetNum;
    public String street;
    public String city;
    public String state;

    public Address(int streetNum, String street, String city, String state) {
        this.streetNum = streetNum;
        this.street = street;
        this.city = city;
        this.state = state;
    }
}
